var modal = document.getElementById("myModal");

// Get modal elements
var modalImg = document.getElementById("modalImage");
var captionText = document.getElementById("modalCaption");
var descriptionText = document.getElementById("modalDescription");
var productPrice = document.getElementById("modalPrice");

// Select all clickable images
var images = document.querySelectorAll(".clickable-image");

// Loop through images and add click event listener
images.forEach(function(img) {
  img.addEventListener("click", function() {
    modal.style.display = "block";
    modalImg.src = this.src;
    captionText.innerHTML = this.alt;

    // Find the closest parent container and get the product description
    var productContainer = this.closest("div"); // Gets the closest wrapping div
    var productDescription = productContainer.querySelector(".product-description");

    // Update the modal description if the element exists
    if (productDescription) {
      descriptionText.innerHTML = productDescription.innerHTML;
    } else {
      descriptionText.innerHTML = "No description available.";
    }

    var itemPrice = productContainer.querySelector(".NewPrice");

    if (itemPrice){
      productPrice.innerHTML = itemPrice.innerHTML;
    } else {
      productPrice.innerHTML = "No price available";
    }
  });
});

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close")[0];

// When the user clicks on <span> (x), close the modal
span.onclick = function() { 
  modal.style.display = "none";
};












































































































































































































































































































































































































































































































































































































































































































































































































































































































































